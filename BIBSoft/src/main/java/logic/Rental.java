package logic;

public class Rental {
	
	private int rentalId;
	private int productId;
	private String type;
	

	//volledige constructor || kan ook voor het wegschrijven van andere producten naar de db
	public Rental(int rentalId, int productId, String type) {
		super();
		this.rentalId = rentalId;
		this.productId = productId;
		this.type = type;
	}
	
	
	//voor een boek dat naar de db geschreven moet worden
	public Rental(int rentalId, int productId) {
		super();
		this.rentalId = rentalId;
		this.productId = productId;
	}



	public int getRentalId() {
		return rentalId;
	}

	public void setRentalId(int rentalId) {
		this.rentalId = rentalId;
	}

	public int getProductId() {
		return productId;
	}

	public void setProductId(int productId) {
		this.productId = productId;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}


}
